package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        int sum = a + b;
        System.out.println(sum);

        int diff = a - b;
        System.out.println(diff);

        int multi = a * b;
        System.out.println(multi);

        int div = a / b;
        System.out.println(div); // 정수 나눗셈

        int mod = a % b;
        System.out.println(mod);
    }
}
